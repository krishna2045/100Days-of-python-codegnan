# while loop
'''
num=1 #variable initialization
while num<11: #condition
    print(num)
    num+=1 #increment/decrement
'''
# print even numbers from 1 to 20

'''
num=1
while num<=20:
    if num%2==0:
        print(num)
    num+=1
'''
# print odd numbers from 1 to 20

'''
num=1
while num<=20:
    if num%2!=0:
        print(num)
    num+=1
'''
