# 2.Comparison/Relational Operators:==,!=,>,<,>=,<=
a=10
b=18
print(a==b)  #False
print(a!=b)  #True
print(a>b)   #False
print(a<b)   #True
print(a>=b)  #False
print(a<=b)  #True

# 3.Logical Operators: and,or,not

